-- -----------------------------------------------------------------------------
-- Function: update_updated_at_column
-- Description: Automatically updates updated_at timestamp on row update
-- -----------------------------------------------------------------------------
CREATE FUNCTION public.update_updated_at_column() RETURNS trigger
    LANGUAGE plpgsql
    AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$;

